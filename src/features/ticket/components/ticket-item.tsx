import { Button } from "@/src/components/ui/button";
import {
  Card,
  CardContent,
  CardFooter,
  CardHeader,
  CardTitle,
} from "@/src/components/ui/card";
import { Ticket } from "@/src/generated/prisma/client";
import { ticketEditPath, ticketPath } from "@/src/paths";
import clsx from "clsx";
import {
  SquareArrowOutUpRight,
  Trash2,
  EllipsisVertical,
  Pencil,
} from "lucide-react";
import Link from "next/link";
import { TICKET_ICONS } from "../../constants";
import deleteTicket from "../actions/delete-ticket";
import { toCurrencyFromCent } from "@/src/utils/currency";
import TicketMoreMenu from "./ticket-more-menu";

type TicketItemProps = {
  ticket: Ticket;
  isDetail?: boolean;
};

const TicketItem = ({ ticket, isDetail }: TicketItemProps) => {
  const detailButton = (
    <Button variant="outline" size="icon" asChild>
      <Link prefetch href={ticketPath(ticket.id)} className="text-sm underline">
        <SquareArrowOutUpRight className="h-4 w-4" />
      </Link>
    </Button>
  );

  const editButton = (
    <Button variant="outline" size="icon" asChild>
      <Link prefetch href={ticketEditPath(ticket.id)}>
        <Pencil />
      </Link>
    </Button>
  );

  const deleteButton = (
    <form action={deleteTicket.bind(null, ticket.id)}>
      <Button variant="outline" size="icon">
        <Trash2 className="h-4 w-4" />
      </Button>
    </form>
  );

  const moreMenu = (
    <TicketMoreMenu
      ticket={ticket}
      trigger={
        <Button variant="outline" size="icon">
          <EllipsisVertical />
        </Button>
      }
      deleteButton={
        <>
          <Trash2 className="h-4 w-4 mr-2" />
          <span>Delete</span>
        </>
      }
    />
  );

  return (
    <>
      <div
        className={clsx("w-full flex gap-x-1", {
          "max-w-[580px]": isDetail,
          "max-w-[420px]": !isDetail,
        })}
      >
        <Card className="w-full" key={ticket.id}>
          <CardHeader>
            <CardTitle className="flex gap-x-2 items-center">
              <span>{TICKET_ICONS[ticket.status]}</span>
              <span className="truncate">{ticket.title}</span>
            </CardTitle>
          </CardHeader>
          <CardContent>
            <span
              className={clsx("whitespace-break-spaces", {
                "line-clamp-3": !isDetail,
              })}
            >
              {ticket.content}
            </span>
          </CardContent>
          <CardFooter className="flex justify-between">
            <p className="text-sm text-muted-foreground">{ticket.deadline}</p>
            <p className="text-sm text-muted-foreground">
              {toCurrencyFromCent(ticket.bounty)}
            </p>
          </CardFooter>
        </Card>

        <div className="flex flex-col gap-y-1">
          {isDetail ? (
            <>
              {editButton}
              {deleteButton}
              {moreMenu}
            </>
          ) : (
            <>
              {detailButton}
              {editButton}
            </>
          )}
        </div>
      </div>
    </>
  );
};

export default TicketItem;
